const Video = ({ setUserSignedIn }) => {
  const navigate = useNavigate();

  const location = useLocation();
  const video = location.state;

  useEffect(() => {
    const username = JSON.parse(localStorage.getItem("username"));
    const password = JSON.parse(localStorage.getItem("password"));

    if (username && password) {
      setUserSignedIn(true);
    } else {
      navigate("/");
    }
  }, []);

  return (
    <Container>
      <Content>
        <VideoWrapper>
          <iframe
            width="100%"
            height="720px"
            src={video.videoLink}
            title="YouTube video player"
            frameborder="0"
            allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture"
            allowfullscreen
          ></iframe>
        </VideoWrapper>
        <Title>{video.title}</Title>
        <Details>
          <Info>
            {video.views} views â€¢ {video.date}
          </Info>
          <Buttons>
            <Button>
              <ThumbDownOffAltOutlinedIcon /> Dislike
            </Button>
            <Button>
              <ReplyOutlinedIcon /> Share
            </Button>
            <Button>
              <AddTaskOutlinedIcon /> Save
            </Button>
          </Buttons>
        </Details>
        <Hr />
        <Channel>
          <ChannelInfo>
            <Image src={video.channelPic} />
            <ChannelDetail>
              <ChannelName>{video.author}</ChannelName>
              <ChannelCounter>{video.subs}</ChannelCounter>
              <Description>
                Lorem ipsum dolor, sit amet consectetur adipisicing elit.
                Doloribus laborum delectus unde quaerat dolore culpa sit aliquam
                at. Vitae facere ipsum totam ratione exercitationem. Suscipit
                animi accusantium dolores ipsam ut.
              </Description>
            </ChannelDetail>
          </ChannelInfo>
          <Subscribe>SUBSCRIBE</Subscribe>
        </Channel>
        <Hr />
        <Comments />
      </Content>

      <Recommendation>
        {STATIC_DATA.filter((item) => {
          return item.author === video.author && item.title !== video.title;
        }).map((item, idx) => {
          return <Card type="sm" key={idx} item={item} />;
        })}
      </Recommendation>
    </Container>
  );
};

export default Video;
